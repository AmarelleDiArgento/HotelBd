package Modelo;

/**
 *
 * @author Amarelle
 */
public class PermisoMod {

    private int id;
    private String modulo;
    private String tabla;
    private String url;

    public PermisoMod(int id, String modulo,String tabla, String url) {
        this.id = id;
        this.modulo = modulo;
        this.tabla = tabla;
        this.url = url;
    }

    public PermisoMod(String modulo, String tabla, String url) {
        this.modulo = modulo;
        this.tabla = tabla;
        this.url = url;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the modulo
     */
    public String getModulo() {
        return modulo;
    }

    /**
     * @param modulo the modulo to set
     */
    public void setModulo(String modulo) {
        this.modulo = modulo;
    }

    /**
     * @return the tabla
     */
    public String getTabla() {
        return tabla;
    }

    /**
     * @param tabla the tabla to set
     */
    public void setTabla(String tabla) {
        this.tabla = tabla;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

}